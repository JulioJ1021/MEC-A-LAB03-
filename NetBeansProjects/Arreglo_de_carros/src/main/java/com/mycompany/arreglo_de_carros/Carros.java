    private void Submit_AtributosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit_AtributosActionPerformed
        //Clase_Carros array_carros[] = new Clase_Carros[num_carros];
        
        num_carros--;
        array_carros[num_carros ] = new Clase_Carros(this.Modelo.getText(),this.Kilometraje.getText(),this.Marca.getText(),this.Color.getText());
        
        this.num_carros_text.setText("# carros:              " + num_carros);
        this.Modelo.setText("");
        this.Kilometraje.setText("");
        this.Marca.setText("");
        this.Color.setText("");
        if(num_carros == 0){
            Color.setVisible(false);
            Marca.setVisible(false);
            Kilometraje.setVisible(false);
            Modelo.setVisible(false);
            Color_text.setVisible(false);
            Marca_text.setVisible(false);
            Kilometraje_text.setVisible(false);
            Modelo_text.setVisible(false);
            Submit_Atributos.setVisible(false);
            if (ordenamiento == "md")
                this.num_carros_text.setText("   Carros ordenados por Modelo :");
            else
                this.num_carros_text.setText("   Carros ordenados por  Kilometraje:");
            Text_area.setVisible(true);
            Ord_txt.setVisible(false);
            for(int c = 0; c < aux; c++){ //intento de ordenamiento
                for(int x = 0; x < (aux - 1); x++){
                    if(array_carros[x].int_modelo() > array_carros[x+1].int_modelo()){
                    orden[x] = c+1;
                    orden[x+1] = c; 
                    }
                }
            }
            for(int c = 0; c < (aux); c++){               
                this.Text_area.setText(this.Text_area.getText() + array_carros[c].mostrar_datos());
            }
        }
               
    }//GEN-LAST:event_Submit_AtributosActionPerformed

